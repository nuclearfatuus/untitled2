#include <iostream>

int main() {
    int a = 7;
    char b = 92;

    std::cout << char(b*2) << std::endl;
    std::cout << char(b/2) << std::endl;
    std::cout << char(b+2) << std::endl;
    std::cout << char(b-2) << std::endl;
    std::cout << a*3 << std::endl;
    std::cout << a/3 << std::endl;
    std::cout << a +3 << std::endl;
    std::cout << a -3 << std::endl;
    return 0;
}